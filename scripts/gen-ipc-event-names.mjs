// Reads shared/ipc_event_names.json and writes src/ipc/generated/eventNames.ts
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const jsonPath = join(root, "shared", "ipc_event_names.json");
const outDir = join(root, "src", "ipc", "generated");
const outPath = join(outDir, "eventNames.ts");

const raw = readFileSync(jsonPath, "utf8");
const map = JSON.parse(raw);
const keys = Object.keys(map).sort();
const lines = [
  "// Generated by scripts/gen-ipc-event-names.mjs from shared/ipc_event_names.json — do not edit.",
  "",
];
for (const k of keys) {
  const v = map[k];
  if (typeof v !== "string") {
    throw new Error(`Expected string for ${k}`);
  }
  lines.push(`export const ${k} = ${JSON.stringify(v)} as const;`);
}
lines.push("");
mkdirSync(outDir, { recursive: true });
writeFileSync(outPath, lines.join("\n"), "utf8");
console.log(`wrote ${outPath}`);
